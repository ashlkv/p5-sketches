const simplex = openSimplexNoise(Date.now());

const cellSize = 20;
const width = 600;
const height = 700;
const noiseIncrement = 0.1;

let grid = []

function setup() {
    createCanvas(width, height);
    grid = [...new Array(height / cellSize + 1)]
        .map((value, row) => [...new Array(width / cellSize + 1)]
        // Simplex noise gives a number between -1 and 1
        .map((value, column) => simplex.noise2D(column * noiseIncrement, row * noiseIncrement)));
}

function draw() {
    background(122);
    for (let row = 0; row < grid.length - 1; row++) {
        for (let column = 0; column < grid[row].length - 1; column++) {
            stroke(grid[row][column] * 255);
            strokeWeight(4);
            point(column * cellSize, row * cellSize, 255)
        }
    }
    
    for (let row = 0; row < grid.length - 1; row ++) {
        for (let column = 0; column < grid[row].length - 1; column ++) {
            // .___a___.
            // |       |
            // d       b
            // |       |
            // .___c___.
            const a = new p5.Vector(column * cellSize + cellSize / 2, row * cellSize);
            const b = new p5.Vector(column * cellSize + cellSize, row * cellSize + cellSize / 2)
            const c = new p5.Vector(column * cellSize + cellSize / 2, row * cellSize + cellSize);
            const d = new p5.Vector(column * cellSize, row * cellSize + cellSize / 2)
            // Math.ceil turns numbers between -1 and 1, generated by simplex noise, to either 0 or 1.
            const northWest = Math.ceil(grid[row][column]);
            const northEast = Math.ceil(grid[row][column + 1]);
            const southEast = Math.ceil(grid[row + 1][column + 1]);
            const southWest = Math.ceil(grid[row + 1][column]);
            const binaryNumber = `${northWest}${northEast}${southEast}${southWest}`;
            const decimalNumber = parseInt(binaryNumber, 2)
            stroke(255);
            strokeWeight(1);
            switch (decimalNumber) {
                case 0:
                case 15:
                    break;
                case 1:
                case 14:
                    connectVectors(c, d);
                    break;
                case 2:
                case 13:
                    connectVectors(b, c);
                    break;
                case 3:
                case 12:
                    connectVectors(b, d);
                    break;
                case 4:
                case 11:
                    connectVectors(a, b);
                    break;
                case 5:
                    connectVectors(b, c);
                    connectVectors(d, a);
                    break;
                case 6:
                case 9:
                    connectVectors(a, c)
                    break;
                case 7:
                case 8:
                    connectVectors(d, a)
                    break;
                case 10:
                    connectVectors(a, b)
                    connectVectors(c, d)
                    break;
            }
        }
    }
}

const connectVectors = (fromVector, toVector) => {
    return line(fromVector.x, fromVector.y, toVector.x, toVector.y);
}
